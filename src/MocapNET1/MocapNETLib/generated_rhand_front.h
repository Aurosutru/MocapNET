static float getJoint2DDistance_RHAND_FRONT(std::vector<float> in,int jointA,int jointB)
 {
    float aX=in[jointA*3+0];
    float aY=in[jointA*3+1];
    float bX=in[jointB*3+0];
    float bY=in[jointB*3+1];
    if ( (aX==0) || (aY==0) || (bX==0) || (bY==0) )
        {return 0.0;}
    return sqrt( ((bX-aX)*(bX-aX)) + ((bY-aY)*(bY-aY)) );
 }

static std::vector<float> rhand_frontCreateNDSM(std::vector<float> in)
 {
    std::vector<float> result;
    for (int i=0; i<MNET_NSDM_RHAND_FRONT_NUMBER; i++)
    {
     unsigned int iJointID=mocapNET_ResolveJoint_rhand_front[i];
     unsigned int iX=in[iJointID*3+0];
     unsigned int iY=in[iJointID*3+1];
     unsigned int iVisibility=in[iJointID*3+2];
     for (int j=0; j<MNET_NSDM_RHAND_FRONT_NUMBER; j++)
     {
       unsigned int jJointID=mocapNET_ResolveJoint_rhand_front[j];
       unsigned int jX=in[jJointID*3+0];
       unsigned int jY=in[jJointID*3+1];
       unsigned int jVisibility=in[jJointID*3+2];
       if ( (iX==0) || (iY==0) || (jX==0) || (jY==0) ) 
       { result.push_back(0.0); result.push_back(0.0); }
       else
       {
       if (mocapNET_ArtificialJoint_rhand_front[i])
          {
           iX+=mocapNET_ArtificialJointXOffset_rhand_front[i];
           iY+=mocapNET_ArtificialJointYOffset_rhand_front[i];
          }
       if (mocapNET_ArtificialJoint_rhand_front[j])
          {
           jX+=mocapNET_ArtificialJointXOffset_rhand_front[j];
           jY+=mocapNET_ArtificialJointYOffset_rhand_front[j];
          }
       result.push_back(iX-jX);
       result.push_back(iY-jY);

       }
     }
    }
  //Normalization
  unsigned int numberOfNSDMScalingRules=5;
      if (numberOfNSDMScalingRules>0)
        {
         unsigned int numberOfDistanceSamples=0;
         float sumOfDistanceSamples=0.0;
         for ( int i=0; i<numberOfNSDMScalingRules; i++) 
            {
             unsigned int iJointID=mocapNET_ScalingStart_rhand_front[i];
             unsigned int jJointID=mocapNET_ScalingEnd_rhand_front[i];
             float distance = getJoint2DDistance_RHAND_FRONT(in,iJointID,jJointID);
             if (distance>0.0)
               {
                numberOfDistanceSamples=numberOfDistanceSamples+1;
                sumOfDistanceSamples=sumOfDistanceSamples+distance;
               }
            }
//------------------------------------------------------------------------------------------------- 
        float scaleDistance=1.0;
//------------------------------------------------------------------------------------------------- 
        if (numberOfDistanceSamples>0)
            { scaleDistance=sumOfDistanceSamples/numberOfDistanceSamples; }
//-------------------------------------------------------------------------------------------------
        if (scaleDistance!=1.0)
            {
             for (int i=0; i<result.size(); i++)
                  { result[i]=result[i]/scaleDistance; } 
            }
//-------------------------------------------------------------------------------------------------
       for (int i=0; i<result.size(); i++)
            {
              if (result[i]!=0.0)
                { result[i]=0.5+result[i]; }
            }
//-------------------------------------------------------------------------------------------------
          }
 return result;
 }
